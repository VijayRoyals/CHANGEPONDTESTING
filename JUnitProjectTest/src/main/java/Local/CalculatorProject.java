package Local;

public class CalculatorProject {
	
	public int add(int a, int b) { 
		return(a+b); 
	}
	public int sub(int x, int y) { 
		return (x-y);  
		
	} 
	public int mul(int q, int p) { 
		return (q*p); 
		
	}
	
	public int div(int r, int s) { 
		return(r/s); 
	}

	public double AreaOfCircle(int r) { 
		return Math.PI*r*r; 
	}
	 
	public boolean positiveNumber() { 
		return true; 
	}
}
